def getDataFramesFromFiles(path='../CollegeScorecard_Raw_Data/'):
    """Creates array of dataframes indexed on year of CollegeScorecard Data

    :param path: Path to unzipped directory of CollegeScorecard data

    :return: an array of data frames indexed by year"""
    
    START_YEAR = 1996
    END_YEAR = 2018
    dfs = {}

    for year in range(START_YEAR, END_YEAR):
        year_YY = year % 100
        dfs[year] = pd.read_csv(path + 'MERGED' + str(year) + '_{:02d}_PP.CSV'.format((year_YY + 1) % 100), delimiter=',', low_memory = False)
    return dfs



def addYearAsLabel(dfs):
    for year in dfs:
        df = dfs[year]
        df['Year'] = year


def dropUselessColumn(dfs):
    for year in dfs:
        df = dfs[year]
        columns_to_drop = academics_to_drop + admissions_to_drop + aid_to_drop + completion_to_drop + cost_to_drop + earnings_to_drop + repayment_to_drop + root_to_drop + school_to_drop + student_to_drop
        df.drop(set(columns_to_drop).intersection(set(df.columns)), axis=1, inplace=True)

def convertUnknownsToNans(dfs):
    for year in dfs:
        df = dfs[year]
        df.replace(to_replace='PrivacySuppressed', value=np.nan, inplace=True)

def dropColsAllNans(dfs):
    for year in dfs:
        df = dfs[year]
        df.dropna(axis=1, how='all', inplace=True)

def convertMixedDataTypes(merged_df):
    for col in merged_df.select_dtypes(include=['object']):
        if col != 'STABBR':
            merged_df[col] = pd.to_numeric(merged_df[col], errors='coerce')
        elif col == 'STABBR':
            merged_df[col] = merged_df[col].astype(str)
    return merged_df


# We have found that, across pub, priv, prog, and other, no rows have any pair of any of these fields populated
# As a result, we can combine these columns without actually adding anything to the numbers recorded. 
# We do this because we already know what kind of institution we are dealing with from other features, so there is
# no harm done in reporting the average net price as one feature. 
# The exception here is PROG, which does not have it's own flag, unlike private public and OTHER
def combine_avg_net_price(merged_df):
    brackets = [ 
                'NPT45_',
                'NPT4_048_', 
                'NPT4_3075_', 
                'NPT4_75UP_']

    inst = ['PUB', 'PRIV', 'OTHER']
    
    for brack in brackets:
        merged_df[brack] = merged_df[brack +inst[0]].fillna(0) \
            + merged_df[brack+inst[1]].fillna(0) + merged_df[brack+inst[2]].fillna(0)
        merged_df = merged_df.drop(brack+inst[0], axis=1)
        merged_df = merged_df.drop(brack+inst[1], axis=1)
        merged_df = merged_df.drop(brack+inst[2], axis=1)
    
    return merged_df

# Because we have varying numbers of rows, we want to get the intersection of all the rows that exist in every year

def intersection_and_merge(dfs):
    init_key = list(dfs.keys())[0]
    init = dfs[init_key]['UNITID']
    intersect_schools = set(init)
    for df in dfs:
        intersect_schools = intersect_schools.intersection(dfs[df]['UNITID'])
        
    # now for getting a new dataframe dictionary containing, for each dataframe, the subset that includes only
    # the rows in intersect_schools
    new_dfs = {}
    for df in dfs:
        indices_keep = []
        for i in range(len(dfs[df])):
            if dfs[df]['UNITID'].iloc[i] in intersect_schools:
                indices_keep.append(i)
        new_dat = dfs[df].iloc[indices_keep]
        new_dfs[df] = new_dat
        
    merged_frame = pd.DataFrame([])
    for df in new_dfs:
        merged_frame = merged_frame.append(new_dfs[df])
    
    merged_frame = merged_frame.reset_index(drop=True)
    
    return merged_frame

def oneHotEncoding(merged_df):
    columns_one_hot = ['STABBR', 'PREDDEG', 'CONTROL', 'HIGHDEG', 'ICLEVEL', 'OPENADMP', 'OPEFLAG', 'SCH_DEG']
    for c in columns_one_hot:
        merged_df = pd.concat([merged_df, pd.get_dummies(merged_df[c], prefix=c)],axis=1)
        return merged_df.drop([c],axis=1, inplace=True)
            
    
